    "def algorythm(row):\n",
    "    prev = 5\n",
    "    nijPrev = 0.6\n",
    "    if (row['up'] > 0 and row['down'] > 0 and row['up']>nijPrev and row['down'] > nijPrev and abs(row['value']-(row['down']+row['up'])/2)>prev):\n",
    "        return abs(row['value']-(row['down']+row['up'])/2)\n",
    "    else:\n",
    "        return 9999\n",
